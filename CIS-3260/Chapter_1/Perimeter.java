package Chapter_1;

public class Perimeter {
	public static void main (String[] args) {
	    double radius;
	    double perimeter;
	    
	    // Assign a radius
	    radius = 10;
	    
	    // Compute perimeter
	    perimeter = 2 * radius * 3.14159;
	    
	    // Display results
	    System.out.println("The perimeter for the circle of radius " +
	      radius + " is " + perimeter);
	  }

}