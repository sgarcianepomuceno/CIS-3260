package Chapter_1;

//This program will print Welcome to Java for the number
//if time that is type in the while statement.
public class StudyWhileLoop {
	public static void main(String[] args) {

//The count is set to 0 to initiate counting from 0.
	int count = 0;

//Count is set to 0 and in this example, while count is less
//than 10, the program will continue running until it reaches 10
//and will print out the following statement.
	while(count < 10) {
		System.out.println("Welcome to Java!");

//After the program has ran once it will add one to
//count will keep adding one until it reaches 10.
		count += 1;
		
		}
	}
}
